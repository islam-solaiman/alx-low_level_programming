#include "3-calc.h"

/**
 * op_add - function return the addition of 2 int.
 * @a: first int.
 * @b: second int.
 * Return: a + b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function return the difference between 2 int.
 * @a: first int.
 * @b: second int.
 * Return: a - b.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function return the muliply of 2 int.
 * @a: first int.
 * @b: second int.
 * Return: a * b.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function return the division of 2 int.
 * @a: first int.
 * @b: second int.
 * Return: a / b.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function return the remainder of the division of 2 int.
 * @a: first int.
 * @b: second int.
 * Return: a % b.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
